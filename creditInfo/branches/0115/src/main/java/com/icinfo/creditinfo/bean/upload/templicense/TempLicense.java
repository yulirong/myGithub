package com.icinfo.creditinfo.bean.upload.templicense;

import java.io.Serializable;
import java.util.Date;

/** 
 * 描述: 行政许可 实体类<br>
 * @author liyafeng
 * @date 2015-11-10
 * 
 */
public class TempLicense implements Serializable
{

    private static final long	serialVersionUID	= 1L;

    // 
    private String id;
    // 企业名称
    private String entName;
    // 工商注册号
    private String regNo;
    // 许可机关
    private String licOrgName;
    // 许可文件编号
    private String licDocNo;
    // 许可文件名称
    private String licDocName;
    // 许可有效期限自
    private Date licValidFrom;
    // 许可有效期限至
    private Date licValidTo;
    // 许可内容
    private String licContent;
    // 状态
    private String state;
    // 
    private String importFrom;
    // 
    private Date importDate;
    // 企业内部序号
    private String corpid;
    //组织机构代码证
    private String orgCode;
    //统一信用代码
    private String unscCode;


    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getEntName() {
        return entName;
    }
    public void setEntName(String entName) {
        this.entName = entName;
    }


    public String getRegNo() {
        return regNo;
    }
    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }


    public String getLicOrgName() {
        return licOrgName;
    }
    public void setLicOrgName(String licOrgName) {
        this.licOrgName = licOrgName;
    }


    public String getLicDocNo() {
        return licDocNo;
    }
    public void setLicDocNo(String licDocNo) {
        this.licDocNo = licDocNo;
    }


    public String getLicDocName() {
        return licDocName;
    }
    public void setLicDocName(String licDocName) {
        this.licDocName = licDocName;
    }


    public Date getLicValidFrom() {
        return licValidFrom;
    }
    public void setLicValidFrom(Date licValidFrom) {
        this.licValidFrom = licValidFrom;
    }


    public Date getLicValidTo() {
        return licValidTo;
    }
    public void setLicValidTo(Date licValidTo) {
        this.licValidTo = licValidTo;
    }


    public String getLicContent() {
        return licContent;
    }
    public void setLicContent(String licContent) {
        this.licContent = licContent;
    }


    public String getState() {
        return state;
    }
    public void setState(String state) {
        this.state = state;
    }


    public String getImportFrom() {
        return importFrom;
    }
    public void setImportFrom(String importFrom) {
        this.importFrom = importFrom;
    }


    public Date getImportDate() {
        return importDate;
    }
    public void setImportDate(Date importDate) {
        this.importDate = importDate;
    }


    public String getCorpid() {
        return corpid;
    }
    public void setCorpid(String corpid) {
        this.corpid = corpid;
    }
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public String getUnscCode() {
		return unscCode;
	}
	public void setUnscCode(String unscCode) {
		this.unscCode = unscCode;
	}
    
}
