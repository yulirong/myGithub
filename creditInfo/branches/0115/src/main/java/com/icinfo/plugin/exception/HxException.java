public class HxException extends Exception 
{
	private static final long	serialVersionUID	= -5595766966440809162L;

	public HxException(){
		super();
	};
	
	public HxException(String vstrThrowsExpType){
		super(vstrThrowsExpType);
	};
	
    public HxException(Throwable vobjThrowable) {
        super(vobjThrowable);
    }
    
	public HxException(Throwable vobjThrowable,String vstrThrowsExpType) 
    {
		super(vstrThrowsExpType,vobjThrowable);
    }

}
