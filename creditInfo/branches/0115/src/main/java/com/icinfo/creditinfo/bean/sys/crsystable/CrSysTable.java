package com.icinfo.creditinfo.bean.sys.crsystable;

import java.io.Serializable;

import org.apache.commons.lang3.builder.ToStringBuilder;

/** 
 * 描述: 数据表菜单维护 实体类<br>
 * @author zhuyong
 * @date 2015-10-10
 * 
 */
public class CrSysTable implements Serializable
{

    private static final long	serialVersionUID	= 1L;

    // 
    private String id;
    // 表编码
    private String tabCode;
    // Bean名称
    private String tabItem;
    // 父节点编码
    private String tabSupCode;
    // 是否有效
    private String tabStatus;
    // 表名称
    private String tabName;
    // 排序序号
    private String tabOrderNo;
    //是否选中 用于数据查询范围选中显示
    private String checked;


    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getTabCode() {
        return tabCode;
    }
    public void setTabCode(String tabCode) {
        this.tabCode = tabCode;
    }


    public String getTabItem() {
        return tabItem;
    }
    public void setTabItem(String tabItem) {
        this.tabItem = tabItem;
    }


    public String getTabSupCode() {
        return tabSupCode;
    }
    public void setTabSupCode(String tabSupCode) {
        this.tabSupCode = tabSupCode;
    }


    public String getTabStatus() {
        return tabStatus;
    }
    public void setTabStatus(String tabStatus) {
        this.tabStatus = tabStatus;
    }


    public String getTabName() {
        return tabName;
    }
    public void setTabName(String tabName) {
        this.tabName = tabName;
    }


    public String getTabOrderNo() {
        return tabOrderNo;
    }
    public void setTabOrderNo(String tabOrderNo) {
        this.tabOrderNo = tabOrderNo;
    }
    
	public String getChecked() {
		return checked;
	}
	public void setChecked(String checked) {
		this.checked = checked;
	}
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}

    
    
}
