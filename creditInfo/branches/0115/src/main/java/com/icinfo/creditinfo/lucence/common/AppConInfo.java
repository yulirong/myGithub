package com.icinfo.creditinfo.lucence.common;

import org.apache.commons.lang3.builder.ToStringBuilder;

/**
 * 描述：返回的企业内容实体
 * 
 * @date 2015-10-19
 * @author 朱勇
 */
public class AppConInfo {
	// 内部序号
	private String corpid;
	// 名称
	private String name;
	// 注册号
	private String regno;
	// 法人代表
	private String legrep;
	//成立日期
	private String estdate;
	//登记机关
	private String regloc;
	
	public String getCorpid() {
		return corpid;
	}
	public void setCorpid(String corpid) {
		this.corpid = corpid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRegno() {
		return regno;
	}
	public void setRegno(String regno) {
		this.regno = regno;
	}
	public String getLegrep() {
		return legrep;
	}
	public void setLegrep(String legrep) {
		this.legrep = legrep;
	}
	public String getEstdate() {
		return estdate;
	}
	public void setEstdate(String estdate) {
		this.estdate = estdate;
	}
	
	public String getRegloc() {
		return regloc;
	}
	public void setRegloc(String regloc) {
		this.regloc = regloc;
	}
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}
}
